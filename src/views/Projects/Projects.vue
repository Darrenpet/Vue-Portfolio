</template>

<script>
export default {
  data() {
    return {
      projects: [],
    };
  },
  mounted() {
    fetch("https://vue-portfolio-api.herokuapp.com/projects", {
      method: "GET",
      headers: {
        "Content-type": "application/json; charset=UTF-8",
      },
    })
      .then((response) => response.json())
      .then((json) => {
        this.projects = json;
      })
      .catch((err) => {
        alert(console.log(err));
      });
  },
};
</script>

